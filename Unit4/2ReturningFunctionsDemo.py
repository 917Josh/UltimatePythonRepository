def greatest_number(num1, num2, num3):
    if num1 > num2 and num1 > num3:
       return(num1)
    elif num2 > num1 and num2 > num3:
        return(num2)
    else:
        return(num3)
print(greatest_number(3, 4, 1))
print(greatest_number(99, -4, 7))
print(greatest_number(0, 0, 0)) 
